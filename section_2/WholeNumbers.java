public class WholeNumbers {
    public static void main(String[] args) {
        int numberOfFans = 5000;
        System.out.println("There are " + numberOfFans + " at the game!");

        // int globalPopulation = 7000000000;
        // int into 2 billion range or 4 bytes in memory
        long globalPopulation = 7800000000L;
        // long up to 9 quintillion
        System.out.println("There are " + globalPopulation + " people on Earth");
        long dailyGoogleSearches = 5000000000L;
        // long up to 9 quintillion or 8 bytes in memory
        System.out.println("There are " + dailyGoogleSearches + " google searches daily");
    }
}
